    private void listMeetingsValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listMeetingsValueChanged
        if (!evt.getValueIsAdjusting()) {
        Object selectedObj = listMeetings.getSelectedValue();
        
        if (selectedObj != null) {
            Meeting selected = (Meeting) selectedObj;
            
            try {
                // 1. Trouver la course (Race) de ce Grand Prix
                int sessionKey = service.fetchRaceSessionKey(selected.getMeetingKey());

                // 2. Télécharger les pilotes ET le classement de la course
                java.util.List<Driver> drivers = service.fetchDrivers(selected.getMeetingKey());
                java.util.List<SessionResult> results = service.fetchRaceResults(sessionKey);

                javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tableDrivers.getModel();
                model.setRowCount(0); // Vide le tableau

                if (results != null && !results.isEmpty()) {
                    // 3. On trie les résultats par position (du 1er au dernier)
                    results.sort((r1, r2) -> {
                        int pos1 = (r1.getPosition() != null) ? r1.getPosition() : 999;
                        int pos2 = (r2.getPosition() != null) ? r2.getPosition() : 999;
                        return Integer.compare(pos1, pos2);
                    });

                    // 4. On remplit le tableau dans l'ordre exact d'arrivée !
                    for (SessionResult result : results) {
                        if (result.getDriverNumber() != null) {
                            for (Driver d : drivers) {
                                if (String.valueOf(d.getDriverNumber()).equals(String.valueOf(result.getDriverNumber()))) {
                                    model.addRow(new Object[]{d.getFullName(), d.getDriverNumber(), d.getTeamName()});
                                    break;
                                }
                            }
                        }
                    }
                } else {
                    // SÉCURITÉ : Si la course n'a pas de classement (ex: essais)
                    java.util.HashSet<Integer> pilotesVus = new java.util.HashSet<>();
                    for (Driver d : drivers) {
                        if (!pilotesVus.contains(d.getDriverNumber())) {
                            pilotesVus.add(d.getDriverNumber());
                            model.addRow(new Object[]{d.getFullName(), d.getDriverNumber(), d.getTeamName()});
                        }
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }
    }//GEN-LAST:event_listMeetingsValueChanged

    private void tableDriversMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDriversMouseClicked
        int viewRow = tableDrivers.getSelectedRow();

if (viewRow != -1) {
    int modelRow = tableDrivers.convertRowIndexToModel(viewRow);
    int driverNumber = (Integer) tableDrivers.getModel().getValueAt(modelRow, 1);
    
    // On garde la session 9157 (GP d'Italie) comme demandé dans la consigne
    // On récupère le Grand Prix que l'utilisateur a sélectionné à gauche
    Object selectedObj = listMeetings.getSelectedValue();
    Meeting selectedMeeting = (Meeting) selectedObj;
    int sessionKey = 9157; // Valeur de secours par défaut
    
    if (selectedMeeting != null) {
        // Le Moteur va chercher la vraie clé de la course pour CE Grand Prix précis !
        sessionKey = service.fetchRaceSessionKey(selectedMeeting.getMeetingKey());
    } 
    
    // ==========================================
    // VERSION 3 : LAPS (CHRONOS)
    // ==========================================
    java.util.List<Lap> laps = service.fetchLaps(sessionKey, driverNumber);
    Double bestLap = Double.MAX_VALUE; 
    Integer maxSpeed = 0;              
    
    for (Lap lap : laps) {
        if (lap.getLapDuration() != null && lap.getLapDuration() < bestLap) {
            bestLap = lap.getLapDuration();
        }
        if (lap.getStSpeed() != null && lap.getStSpeed() > maxSpeed) {
            maxSpeed = lap.getStSpeed();
        }
    }
    
    if (bestLap == Double.MAX_VALUE) {
        lblmeilleur_tour.setText("Meilleur tour : Aucun chrono");
    } else {
        lblmeilleur_tour.setText("Meilleur tour : " + bestLap + " s");
    }
    
    if (maxSpeed == 0) {
        lblvitesse_max.setText("Vitesse max : Inconnue");
    } else {
        lblvitesse_max.setText("Vitesse max : " + maxSpeed + " km/h");
    }

    // ==========================================
    // VERSION 4 : TELEMETRIE (VOITURE)
    // ==========================================
    java.util.List<CarData> telemetry = service.fetchCarData(sessionKey, driverNumber);
    
    if (!telemetry.isEmpty()) {
        // L'API renvoie des milliers de points. Pour le spectacle, 
        // on va chercher le moment exact où le pilote allait le plus vite !
        CarData topData = telemetry.get(0);
        for (CarData data : telemetry) {
            if (data.getSpeed() != null && topData.getSpeed() != null && data.getSpeed() > topData.getSpeed()) {
                topData = data;
            }
        }
        
        // 1. Mise à jour de la vitesse
        lblSpeed.setText("Vitesse Télémétrie : " + topData.getSpeed() + " km/h");
        
        // 2. Mise à jour du compte-tours (Jauge)
        if (topData.getRpm() != null) {
            barRpm.setValue(topData.getRpm());
            barRpm.setString(topData.getRpm() + " tr/min"); // Transforme le "0 %" en "12000 tr/min"
        }
        
        // 3. Mise à jour du DRS (Dans l'API, une valeur >= 8 veut dire aileron ouvert)
        if (topData.getDrs() != null && topData.getDrs() >= 8) {
            lblDrs.setText("DRS : Ouvert 🟩");
        } else {
            lblDrs.setText("DRS : Fermé 🟥");
        }
    } else {
        lblSpeed.setText("Vitesse Télémétrie : Aucune donnée");
        barRpm.setValue(0);
        barRpm.setString("0 tr/min");
        lblDrs.setText("DRS : -");
    }
    // ==========================================
    // VERSION 5 : DESSIN DU CIRCUIT
    // ==========================================
    java.util.List<Location> locations = service.fetchLocations(sessionKey, driverNumber);
    circuitPanel.setLocations(locations);
}
    }//GEN-LAST:event_tableDriversMouseClicked

    
