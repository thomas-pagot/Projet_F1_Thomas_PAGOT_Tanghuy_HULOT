    private void btnComparerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComparerActionPerformed
        // 1. On vérifie qu'un pilote (Pilote 1) est bien sélectionné
    int viewRow = tableDrivers.getSelectedRow();
    
    if (viewRow != -1) {
        int modelRow = tableDrivers.convertRowIndexToModel(viewRow);
        String nomPilote1 = tableDrivers.getModel().getValueAt(modelRow, 0).toString();
        int numPilote1 = (Integer) tableDrivers.getModel().getValueAt(modelRow, 1);
        
        // On récupère le Grand Prix actuel pour avoir la bonne session
        Object selectedObj = listMeetings.getSelectedValue();
        Meeting selectedMeeting = (Meeting) selectedObj;
        if (selectedMeeting == null) return;
        int sessionKey = service.fetchRaceSessionKey(selectedMeeting.getMeetingKey());

        // 2. On demande à l'utilisateur contre qui il veut comparer (Pilote 2)
        String input = javax.swing.JOptionPane.showInputDialog(this, 
                "Contre qui voulez-vous comparer " + nomPilote1 + " ?\nEntrez le NUMÉRO du 2ème pilote (ex: 44, 1, 16...) :");

        // Si l'utilisateur a tapé quelque chose et n'a pas annulé
        if (input != null && !input.trim().isEmpty()) {
            try {
                int numPilote2 = Integer.parseInt(input.trim());

                // 3. On télécharge les chronos des deux pilotes !
                java.util.List<Lap> laps1 = service.fetchLaps(sessionKey, numPilote1);
                java.util.List<Lap> laps2 = service.fetchLaps(sessionKey, numPilote2);

                // 4. On trouve le meilleur tour du Pilote 1
                Double bestLap1 = Double.MAX_VALUE;
                for (Lap lap : laps1) {
                    if (lap.getLapDuration() != null && lap.getLapDuration() < bestLap1) {
                        bestLap1 = lap.getLapDuration();
                    }
                }

                // 5. On trouve le meilleur tour du Pilote 2
                Double bestLap2 = Double.MAX_VALUE;
                for (Lap lap : laps2) {
                    if (lap.getLapDuration() != null && lap.getLapDuration() < bestLap2) {
                        bestLap2 = lap.getLapDuration();
                    }
                }

                // 6. On prépare l'affichage
                String txt1 = (bestLap1 == Double.MAX_VALUE) ? "Aucun temps" : bestLap1 + " s";
                String txt2 = (bestLap2 == Double.MAX_VALUE) ? "Aucun temps" : bestLap2 + " s";

                String vainqueur = "";
                if (bestLap1 < bestLap2) {
                    vainqueur = "🏆 Vainqueur : " + nomPilote1 + " !";
                } else if (bestLap2 < bestLap1) {
                    vainqueur = "🏆 Vainqueur : Pilote N°" + numPilote2 + " !";
                } else {
                    vainqueur = "🤝 Égalité ou données manquantes !";
                }

                // 7. On affiche le grand résultat final
                String message = "=== COMPARAISON DES MEILLEURS TOURS ===\n\n"
                               + "Pilote 1 (" + nomPilote1 + ") : " + txt1 + "\n"
                               + "Pilote 2 (N°" + numPilote2 + ") : " + txt2 + "\n\n"
                               + vainqueur;

                javax.swing.JOptionPane.showMessageDialog(this, message, "Résultat du Duel", javax.swing.JOptionPane.INFORMATION_MESSAGE);

            } catch (NumberFormatException e) {
                // Sécurité au cas où l'utilisateur tape des lettres au lieu d'un numéro
                javax.swing.JOptionPane.showMessageDialog(this, "Veuillez entrer un numéro de pilote valide (chiffres uniquement).", "Erreur", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Veuillez d'abord sélectionner le Pilote 1 dans le tableau !");
    }   
    }//GEN-LAST:event_btnComparerActionPerformed



